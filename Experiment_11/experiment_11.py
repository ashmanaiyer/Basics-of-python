import pandas as pd

data={
'Order ID':['01','02','03','04','05','06','07','08','09','010'],
'Category':['Electronics','Clothing','Electronics','Grocery','Clothing','Electronics','Grocery','Clothing','Electronics','Grocery'],
'Payment Method':['Cash','Card','UPI','Card','Cash','UPI', None, None, None, None],
'Delivery type':['Standard','Express','Standard','Express','Standard','Express', None, None, None, None],
'Customer type':['New','Existing','New','Existing','New','Existing', None, None, None, None]
}
df=pd.DataFrame(data)
print(df)

#1. Frequency Count
#Count how many order in each category
print(df['Category'].value_counts())

#2.Unique Values
#Display all uniques cateories
print(df['Category'].unique())

#3. Number of Unique Values
print(df['Category'].nunique())

#4. Frequency of Payment methods
print(df['Payment Method'].value_counts())

#5. Cross Tabulation
# Category vs Payment Method
print(pd.crosstab(df['Category'],df['Payment Method']))

#6. Cross Tabulation (Delivery vs Customer Type)
print(pd.crosstab(df['Delivery type'],df['Customer type']))

#7. Percentage Distribuion
print(df['Category'].value_counts(normalize=True)*100)

#8. Filtering Data
#Example: Orders from Electronics category
electronics_orders=df[df['Category']=='Electronics']
print(electronics_orders)

#9. Orders from clothing category
clothing_orders=df[df['Category']=='Clothing']
print(clothing_orders)

#10. Grouping
#Orders by category and payment method
print(df.groupby('Category')['Payment Method'].value_counts())

#11. Sorting Data
sorted_df=df.sort_values(by='Category')
print(sorted_df)

